func isValidLanguageCode(lang string) bool {
	if _, ok := iso6931languages[lang]; ok {
		return true
	}
	if _, ok := iso6932languages[lang]; ok {
		return true
	}
	return false
}

func iso6933toIso6931(iso3 string) string {
	if iso1, ok := iso6933toiso6931[iso3]; ok {
		return iso1
	}
	return iso3
}
